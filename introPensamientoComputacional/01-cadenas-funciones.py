my_str = 'Hello, world!' 

#Longitud de cadena
print(len(my_str))

#Buscamos el caracter en la posicion 
print(my_str[0])
print(my_str[1])
print(my_str[2])

#division de cadena en subcadena

#Desde el caracter hasta el final 
print(my_str[2:])

#Desde el inicio hasta el caracter 
print(my_str[:5])

#Dividir segun los caracteres
print(my_str[4:8])

#Traemos todo el string dando saltos segun el caracter
print(my_str[::2])
print(my_str[::3])